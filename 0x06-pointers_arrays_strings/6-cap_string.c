/**
 * cap_string - capitalizes all words of a string
 * @s: string
 *
 * Return: capitalized string
 */

char *cap_string(char *s)
{
	int i = 0;

	if (s[i] >= 'a' && s[i] <= 'z')
		s[i] = s[i] - ' '; /* a check 1st character */
	i++;

	while (s[i] != '\0') /* loop through the remaining characters */
	{

		/* capitalize all lowercase that comes after a special char*/
		if ((s[i] >= 'a' && s[i] <= 'z')
		    && (s[i - 1] == ',' || s[i - 1] == ';' || s[i - 1] == '.' ||
			s[i - 1] == '!' || s[i - 1] == '?' || s[i - 1] == '"' ||
			s[i - 1] == '(' || s[i - 1] == ')' || s[i - 1] == '{' ||
			s[i - 1] == '}' || s[i - 1] == ' ' || s[i - 1] == '\t'
			|| s[i - 1] == '\n'))
			s[i] = s[i] - ' ';
		i++;
	}

	return (s);
}
